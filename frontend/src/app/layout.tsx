// polish-2 T45 — above-the-fold critical CSS, inlined at the top of
// <body> to kill FOUC. Generated by `frontend/scripts/extract-critical-css.mjs`
// and read at server-render time (Next.js layouts are server components by
// default, so `fs.readFileSync` runs once per build / hot-reload).
import { readFileSync } from "node:fs";
import { join } from "node:path";
const criticalCss: string = (() => {
  try {
    return readFileSync(
      join(process.cwd(), "src/app/styles/critical.css"),
      "utf-8",
    );
  } catch {
    // Build hasn't run yet (or running in vitest with mocked cwd) —
    // ship an empty string; the global stylesheet still ships the rules.
    return "";
  }
})();

