export default function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) {
  return (
    <html lang="en" suppressHydrationWarning>
      <head>
        {/* SSR-safe theme boot. Reads localStorage["gowork-theme"] and
         * writes data-theme on <html> BEFORE React hydrates so the first
         * paint matches the user's saved preference and there's no flash
         * from dark→light (or vice versa) when SiteHeader hydrates and
         * runs its own theme mirror effect. Falls back to the system's
         * prefers-color-scheme on first visit. */}
        <script
          // eslint-disable-next-line react/no-danger
          dangerouslySetInnerHTML={{
            __html: `
              (function() {
                try {
                  // polish-3 round-3 — DARK is the canonical brand mode.
                  // Light is opt-in via the SiteHeader toggle ONLY (writes
                  // localStorage). We don't follow prefers-color-scheme any
                  // more because the editorial color story (warm-paper text
                  // on dark navy + cyan / amber path) only resolves in dark.
                  // A user whose OS is on light mode would otherwise land
                  // on a cream version of the page that looks broken.
                  var stored = window.localStorage.getItem('gowork-theme');
                  var theme = stored === 'light' ? 'light' : 'dark';
                  var html = document.documentElement;
                  html.setAttribute('data-theme', theme);
                  if (theme === 'dark') html.classList.add('dark');
                  else html.classList.remove('dark');
                } catch (e) { /* localStorage blocked — leave default */ }
              })();
            `,
          }}
        />
      </head>
      <body className={`${inter.variable} font-sans antialiased`}>
        {/* polish-2 T45 — inline above-the-fold critical CSS. Sits at
            the top of <body> so the first paint already has Ch1 hero +
            chrome rules without waiting on the global stylesheet
            roundtrip. */}
        <style
          data-critical-css
          dangerouslySetInnerHTML={{ __html: criticalCss as string }}
        />
        <Providers>
          <MotionProvider>
            <SmoothScroll>
              <AriaLiveProvider>
                <ScrollProgress />
                <ViewTransitionsProvider>
                  <TranslationProvider>
                    <SkipToContent />
                    {/* Universal site header — was previously the
                     *  legacy `<Header />` from `components/layout/Header`,
                     *  which still showed the legacy /
                     *  workforce-companion nav on every subpage even
                     *  though the home page rendered the new
                     *  facelift `<SiteHeader />`. ChromeFrame still
                     *  suppresses the layout chrome on `/` so the
                     *  HomePage's own internal `<SiteHeader />` stays
                     *  the only header on the home route — every other
                     *  route now also gets the same SiteHeader, so
                     *  the chrome is consistent across the entire
                     *  app. */}
                    <ChromeFrame>
                      <SiteHeader />
                    </ChromeFrame>
                    <ErrorBoundary>
                      <main
                        id="main"
                        className="flex min-h-[calc(100vh-3.5rem)] flex-col"
                      >
                        <div className="flex-1">{children}</div>
                        <ChromeFrame>
                          <Footer />
                        </ChromeFrame>
                      </main>
                    </ErrorBoundary>
                    <AriaLiveRegion />
                  </TranslationProvider>
                </ViewTransitionsProvider>
              </AriaLiveProvider>
            </SmoothScroll>
          </MotionProvider>
        </Providers>
      </body>
    </html>
  );
}
