#!/usr/bin/env node
/**
 * W5 Driver D — Spotlight invention.
 *
 * scripts/new-city-scaffold.mjs
 *
 * Generates the file scaffold for adding a new city to GoWork.
 * Produces:
 *
 *   frontend/src/lib/cities/<slug>.ts             (city config stub)
 *   frontend/data/<slug>/barriers.json            (barrier graph stub)
 *   frontend/src/lib/translations/cities/<slug>.en.json
 *   frontend/src/lib/translations/cities/<slug>.es.json
 *   frontend/src/__tests__/cities/<slug>.test.ts  (guard test)
 *
 * Usage:
 *
 *   node scripts/new-city-scaffold.mjs --slug=dallas --name="Dallas, TX" --state=TX
 *   node scripts/new-city-scaffold.mjs --slug=birmingham --name="Birmingham, AL" --state=AL --lng=-86.802 --lat=33.518 --zoom=11
 *
 * Idempotent: pass --force to overwrite existing files.
 *
 * Documented in `docs/multi-city-expansion-playbook.md`.
 */
import { existsSync, mkdirSync, writeFileSync } from "node:fs";
import { join, dirname, resolve } from "node:path";
import { argv, exit } from "node:process";

const REPO_ROOT = resolve(import.meta.dirname ?? new URL(".", import.meta.url).pathname, "..");

// ─────────────────────────────────────────────────────────────
// Arg parsing
// ─────────────────────────────────────────────────────────────
const args = argv.slice(2);
function flagValue(name, alias) {
  for (const a of args) {
    if (a.startsWith(`--${name}=`)) return a.slice(name.length + 3);
    if (alias && a.startsWith(`--${alias}=`)) return a.slice(alias.length + 3);
  }
  return undefined;
}
const force = args.includes("--force");
const help = args.includes("--help") || args.includes("-h");

if (help) {
  console.log(`
new-city-scaffold.mjs — generate the scaffold for a new GoWork city.

Required:
  --slug=<slug>         URL-safe slug (e.g., dallas, birmingham)
  --name="<full name>"  Human-readable name (e.g., "Dallas, TX")
  --state=<XX>          Two-letter state code (e.g., TX, AL)

Optional:
  --lng=<float>         Center longitude (default: 0)
  --lat=<float>         Center latitude (default: 0)
  --zoom=<int>          Default zoom (default: 11)
  --force               Overwrite existing files

See docs/multi-city-expansion-playbook.md for the full deployment
process; this script just stamps out the file shape.
`);
  exit(0);
}

const SLUG = flagValue("slug");
const NAME = flagValue("name");
const STATE = flagValue("state");
const LNG = flagValue("lng") ?? "0";
const LAT = flagValue("lat") ?? "0";
const ZOOM = flagValue("zoom") ?? "11";

if (!SLUG || !NAME || !STATE) {
  console.error("Missing required flags. Usage:");
  console.error('  node scripts/new-city-scaffold.mjs --slug=<slug> --name="<full>" --state=<XX>');
  console.error("Pass --help for full usage.");
  exit(2);
}

if (!/^[a-z][a-z0-9-]*$/.test(SLUG)) {
  console.error(`Invalid --slug: "${SLUG}". Must be lowercase, start with a letter, alphanumeric + dashes only.`);
  exit(2);
}

if (!/^[A-Z]{2}$/.test(STATE)) {
  console.error(`Invalid --state: "${STATE}". Must be 2 uppercase letters (e.g., TX, AL).`);
  exit(2);
}

// ─────────────────────────────────────────────────────────────
// Templates
// ─────────────────────────────────────────────────────────────
const cityConfigPath = join(REPO_ROOT, "frontend", "src", "lib", "cities", `${SLUG}.ts`);
const barriersPath = join(REPO_ROOT, "frontend", "data", SLUG, "barriers.json");
const enTransPath = join(REPO_ROOT, "frontend", "src", "lib", "translations", "cities", `${SLUG}.en.json`);
const esTransPath = join(REPO_ROOT, "frontend", "src", "lib", "translations", "cities", `${SLUG}.es.json`);
const testPath = join(REPO_ROOT, "frontend", "src", "__tests__", "cities", `${SLUG}.test.ts`);

const CITY_CONFIG_TS = `/**
 * GoWork — City config for ${NAME}.
 *
 * Generated by scripts/new-city-scaffold.mjs on the W5 Driver D
 * Spotlight pattern. See docs/multi-city-expansion-playbook.md for
 * the full deployment process.
 */
import type { CityConfig } from "./types";

export const ${SLUG.toUpperCase().replace(/-/g, "_")}_CONFIG: CityConfig = {
  slug: "${SLUG}",
  name: "${NAME}",
  state: "${STATE}",
  centerLng: ${LNG},
  centerLat: ${LAT},
  defaultZoom: ${ZOOM},
  // TODO: replace with your Mapbox style URL after creating one in Studio.
  mapboxStyleUrl: "mapbox://styles/<your-account>/<style-id>",
  landmarks: {
    // TODO: fill with city-specific landmarks. Each chapter references
    // these by id (e.g., camera target for Ch2 may use "downtown").
    // downtown: { lng: ${LNG}, lat: ${LAT} },
  },
  // TODO: city's transit provider (e.g., DART for Dallas, Trinity Metro
  // for Fort Worth, MAX for Montgomery).
  transitProvider: "TODO",
};
`;

const BARRIERS_JSON = `{
  "_comment": "GoWork barrier graph for ${NAME}. Generated by new-city-scaffold.mjs. See docs/multi-city-expansion-playbook.md.",
  "_state": "${STATE}",
  "_city": "${SLUG}",
  "barriers": [],
  "edges": []
}
`;

const EN_TRANS_JSON = `{
  "_comment": "EN translations for ${NAME}. Generated by new-city-scaffold.mjs.",
  "city": "${NAME.split(",")[0].trim()}",
  "cityFull": "${NAME}",
  "transitProvider": "TODO",
  "landmarks": {
    "_TODO": "Fill in EN strings for each landmark in the city config."
  }
}
`;

const ES_TRANS_JSON = `{
  "_comment": "ES translations for ${NAME}. Generated by new-city-scaffold.mjs. Parity with EN is enforced.",
  "city": "${NAME.split(",")[0].trim()}",
  "cityFull": "${NAME}",
  "transitProvider": "TODO",
  "landmarks": {
    "_TODO": "Fill in ES strings for each landmark in the city config."
  }
}
`;

const TEST_TS = `/**
 * Guard test for ${NAME} city scaffold.
 * Generated by scripts/new-city-scaffold.mjs.
 *
 * This test fails until the operator fills in the TODO fields. That's
 * intentional — it forces a deliberate completion of the city
 * deployment before merge.
 */
import { describe, it, expect } from "vitest";
import { ${SLUG.toUpperCase().replace(/-/g, "_")}_CONFIG } from "@/lib/cities/${SLUG}";

describe("${NAME} — city config (Driver D scaffold)", () => {
  it("declares the city slug + name + state", () => {
    expect(${SLUG.toUpperCase().replace(/-/g, "_")}_CONFIG.slug).toBe("${SLUG}");
    expect(${SLUG.toUpperCase().replace(/-/g, "_")}_CONFIG.name).toBe("${NAME}");
    expect(${SLUG.toUpperCase().replace(/-/g, "_")}_CONFIG.state).toBe("${STATE}");
  });

  it.skip("[fill me in] mapboxStyleUrl is a real published Mapbox style", () => {
    // TODO: un-skip after creating a Mapbox style for ${NAME}.
    expect(${SLUG.toUpperCase().replace(/-/g, "_")}_CONFIG.mapboxStyleUrl).not.toMatch(/your-account|TODO/);
  });

  it.skip("[fill me in] landmarks include at least 3 entries", () => {
    // TODO: un-skip after filling in landmarks.
    expect(Object.keys(${SLUG.toUpperCase().replace(/-/g, "_")}_CONFIG.landmarks).length).toBeGreaterThanOrEqual(3);
  });

  it.skip("[fill me in] transitProvider is set", () => {
    // TODO: un-skip after setting the transit provider name.
    expect(${SLUG.toUpperCase().replace(/-/g, "_")}_CONFIG.transitProvider).not.toBe("TODO");
  });
});
`;

// ─────────────────────────────────────────────────────────────
// Write
// ─────────────────────────────────────────────────────────────
function writeFile(path, content) {
  if (existsSync(path) && !force) {
    console.warn(`[scaffold] SKIP (exists): ${path}`);
    return;
  }
  mkdirSync(dirname(path), { recursive: true });
  writeFileSync(path, content, "utf8");
  console.log(`[scaffold] wrote: ${path}`);
}

writeFile(cityConfigPath, CITY_CONFIG_TS);
writeFile(barriersPath, BARRIERS_JSON);
writeFile(enTransPath, EN_TRANS_JSON);
writeFile(esTransPath, ES_TRANS_JSON);
writeFile(testPath, TEST_TS);

console.log("");
console.log(`[scaffold] Done. Next steps for ${NAME}:`);
console.log(`  1. Edit ${cityConfigPath} — fill landmarks + Mapbox style URL.`);
console.log(`  2. Build a Mapbox style at https://studio.mapbox.com/.`);
console.log(`  3. Fill ${barriersPath} with the city's barrier graph.`);
console.log(`  4. Fill ${enTransPath} + ${esTransPath} (parity required).`);
console.log(`  5. Register the config in frontend/src/lib/cities/registry.ts.`);
console.log(`  6. Run: cd frontend && npx vitest run src/__tests__/cities/${SLUG}.test.ts`);
console.log(`  7. See docs/multi-city-expansion-playbook.md for production polish.`);
